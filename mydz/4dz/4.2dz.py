place = int(input("Введите номер места (1-54): "))

if 1 <= place <= 36:
    # купе
    if place % 2 == 0:
        print("Нижнее место в купе")
    else:
        print("Верхнее место в купе")
elif 37 <= place <= 54:
    # боковые
    if place % 2 == 0:
        print("Нижнее боковое место")
    else:
        print("Верхнее боковое место")
else:
    print("Нет такого места в плацкартном вагоне")